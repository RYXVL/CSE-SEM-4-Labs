; Convert a 32 bit packed BCD number into its equivalent hexadecimal number.
	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =NUM
	LDR R1, =RESULT
	LDR R2, [R0]
	MOV R8, #10
	MOV R3, #1 ; POWER OF 10
	MOV R5, #0 ; COUNTER FOR NUMBER OF DIGITS IN BCD
	MOV R4, R2 ; COPY OF MAIN NUMBER
LEN ADD R5, R5, #1 ; CALCULATES THE LENGTH OF THE NUMBER
	MOVS R4, R4, LSR #4
	BNE LEN
UP	AND R6, R2, #15
	MLA R7, R3, R6, R7
	MOV R2, R2, LSR #4
	MUL R3, R3, R8
	SUBS R5, R5, #1
	BNE UP
	STR R7, [R1]
STOP B STOP
NUM DCD 0X539
	AREA mydata, DATA, READWRITE
RESULT DCD 0
	END
