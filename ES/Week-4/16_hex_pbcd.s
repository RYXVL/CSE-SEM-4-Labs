; Convert a 16 bit hex number into its equivalent packed BCD.
	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0, =NUM
	LDR R1, =RESULT
	LDR R2, [R0]
	LDR R3, =0XA
	LDR R5, =0X4
	MOV R4, R2
UP	MOV R8, #0
	BL DIV
	CMP R8, #0
	BEQ FIN2
UP1 MOV R9, R9, LSL R5
	ADD R9, R9, R8
	B UP
DIV CMP R4, R3
	BCC UP2
	SUB R4, R4, R3
	ADD R8, R8, #1
	CMP R4, R3
	BHS UP
UP2	BX LR
FIN	STR R9, [R1]
FIN2 MOV R9, R9, LSL R5
	ADD R9, R9, R4
	STR R9, [R1]
STOP B STOP
NUM DCD 0XC
	AREA mydata, DATA, READWRITE
RESULT DCD 0
	END
