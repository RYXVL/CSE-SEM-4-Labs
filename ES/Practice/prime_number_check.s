; Program to find if a number is prime or not.
	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
X	LDR R0, =N
	LDR R1, [R0]
	LDR R2, =RESULT
	MOV R11, #1
	MOV R4, #2 	;DIVISOR
UP	MOV R3, R1 	;COPY OF NUMBER
	MOV R10, #0 ;QUOTIENT
	BL DIV
	CMP R3, #0	;CHECKING IS PERFECTLY DIVIDED
	BEQ STOP
	ADD R4, R4, #1	;INCREMENT DIVISOR
	CMP R4, R1	;EXIT IF NEW DIVISOR EQUAL TO DIVIDENT
	BNE UP
	B ANS
DIV	SUB R3, R3, R4
	ADD R10, R10, #1
	CMP R3, R4
	BHS DIV
	BX LR
ANS STR R11, [R2]
STOP B STOP
N	DCD 0XD
	AREA mydata, DATA, READWRITE
RESULT DCD 0
	END
